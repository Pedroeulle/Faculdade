print(" Faça um programa que receba o raio de uma circunferência, calcule e mostre:\n")

r17aio = float(input("digite um raio: "))

print("\n a) O comprimento da circunferência, usando a fórmula 2⋅π⋅raio:\n")
c17ump = 2 * 3.14 * r17aio
print (f"O comprimento da circunferência é: {c17ump :.2f}\n")

print("b) A área da circunferência, usando a fórmula π⋅raio²:\n")
a17rea = 3.14 * r17aio **2
print (f"A área da circunferência é: {a17rea :.2f}\n")

print("c) O volume de uma esfera com mesmo raio, usando a fórmula 4⋅π⋅raio³ /3 \n")
v17olum = 4 * 3.14 * r17aio **3 / 3
print (f"O volume de uma esfera com mesmo raio é: {v17olum :.2f}\n \n")
