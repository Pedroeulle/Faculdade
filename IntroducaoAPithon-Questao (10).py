print(" Faça um programa que calcule e mostre a área de um trapézio. Sabe-se que área dotrapézio= area = (base maior + base menor)*altura/2 . Seu programa deve receber os valores para a base maior, base menor e altura. \n")

baseMai = float(input("Digite o valor da base maior do trapézio: "))
baseMen = float (input("Digite o valor da base menor do trapézio: "))
alt = float(input("Digite a altura do trapézio: "))
R10esul = ((baseMai + baseMen) * alt) / 2
print(f"A Área do trapézio é: {R10esul}\n \n")
