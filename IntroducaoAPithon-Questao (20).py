print(" Faça um programa que receba o número de lados de um polígono convexo, calcule e mostre o número de diagonais desse polígono. Sabe-se que o número de diagonais é dado pela fórmula N⋅(N−3) / 2 onde N é o número de lados do polígono.: \n")

n20um = int(input("Digite o numero de lados do poligonos: \n"))
r20esul = n20um * (n20um - 3) / 2 
print(f"O número de diagonais desse polígono é: {r20esul} \n \n")
